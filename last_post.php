        <h3>19.&nbsp;października 2025&nbsp;r.</h3>
        <h3>XXIX&nbsp;Niedziela Zwykła, Rok&nbsp;C</h3>
    </div>
    <div class="col">
        <p>1. Trwa październik, miesiąc modlitwy różańcowej. Zapraszamy
        do&nbsp;wspólnej modlitwy każdego dnia na&nbsp;godzinę 17.30.</p>
        <p>2. Dzisiaj rozpoczyna się tydzień misyjny, dlatego naszą modlitwę
        w&nbsp;tych dniach będziemy ofiarować szczególnie w&nbsp;intencji
        misjonarzy i&nbsp;misji świętych. Dzisiejsza taca przeznaczona jest
        na&nbsp;cele misyjne.</p>
        <p>3. W&nbsp;tym tygodniu czcimy świętych: w&nbsp;poniedziałek
        świętego Jana Kantego, w&nbsp;sobotę świętego Jana Pawła&nbsp;II.
        Wspomnienie liturgiczne świętego Jana Pawła&nbsp;II obchodzone jest
        w&nbsp;polskim Kościele dnia 22&nbsp;października. Jednak tego dnia
        w&nbsp;naszej diecezji obchodzimy święto rocznicy poświęcenia Bazyliki
        Katedralnej. Stąd przeniesienie wspomnienia Papieża Polaka
        na&nbsp;sobotę 25&nbsp;października.</p>
        <p>4. Ksiądz Paweł Lasek, wykładowca Pisma Świętego w&nbsp;naszym
        seminarium, zaprasza serdecznie, jak w&nbsp;każdy trzeci wtorek
        miesiąca, na&nbsp;kolejne spotkanie Akademii Biblijnej
        w&nbsp;Sandomierzu. Temat spotkania w&nbsp;najbliższy <strong>wtorek
        21&nbsp;października</strong> to&nbsp;Listy św.&nbsp;Pawła
        do&nbsp;Tesaloniczan. Początek o&nbsp;godz.&nbsp;19.30, w&nbsp;auli
        Wyższego Seminarium Duchownego.</p>
        <p>5. W&nbsp;piątek zapraszamy na&nbsp;spotkanie scholi
        o&nbsp;godz.&nbsp;16.30 w&nbsp;Sali na&nbsp;plebanii.</p>
        <p>6. Dnia <strong>25&nbsp;października</strong> w&nbsp;Sandomierzu
        odbędzie się Diecezjalne Spotkanie Wojowników Maryi. Rozpoczęcie Mszą&nbsp;św.
        o&nbsp;godz.&nbsp;11.00 w&nbsp;bazylice katedralnej pod przewodnictwem
        Biskupa Ordynariusza, następnie procesja różańcowa, która zakończy się
        w&nbsp;naszym kościele parafialnym. Zapraszamy
        na&nbsp;to&nbsp;wydarzenie wszystkich: mężczyzn, ale także kobiety
        i&nbsp;dzieci.</p>
        <p>7. Również w&nbsp;przyszłą sobotę - <strong>25&nbsp;października</strong>
        - zapraszamy na&nbsp;kolejny Wieczór Uwielbienia w&nbsp;naszym kościele
        parafialnym. Początek o&nbsp;godz.&nbsp;18.00.</p>
        <p>8. Diecezjalny Duszpasterz Rodzin, a&nbsp;zarazem nasz Wikariusz
        ksiądz Krystian, zaprasza na&nbsp;Jubileuszową Pielgrzymkę Małżeństw
        i&nbsp;Rodzin, która odbędzie się w&nbsp;przyszłą niedzielę -
        <strong>26&nbsp;października</strong> w&nbsp;Sanktuarium
        w&nbsp;Sulisławicach. O&nbsp;godz.&nbsp;12.30 Msza Święta
        z&nbsp;odnowieniem przyrzeczeń małżeńskich. Szczegóły
        na&nbsp;plakacie.</p>
        <p>9. <strong>Przyszłą niedzielę</strong> obchodzić będziemy jako
        uroczystość rocznicy poświęcania naszego kościoła parafialnego.
        Chcemy Panu Bogu podziękować za&nbsp;to&nbsp;miejsce, w&nbsp;którym
        gromadzimy się na&nbsp;modlitwie, za&nbsp;budowniczych kościoła,
        za&nbsp;wszystkich parafian, którzy swoją pracą i&nbsp;ofiarnością
        doprowadzili do&nbsp;powstania naszej świątyni. W&nbsp;tym dniu
        na&nbsp;Mszy świętej o&nbsp;godzinie 11.15 chcemy też wyrazić
        wdzięczność księdzu Proboszczowi Seniorowi Józefowi Śmiglowi, który
        w&nbsp;ten rok przeżywa jako jubileuszowy 50&nbsp;rok swojego
        kapłaństwa. Zapraszamy do&nbsp;wspólnej modlitwy.</p>
        <p>10. W&nbsp;dalszym ciągu przyjmujemy wypominki jednorazowe
        (na&nbsp;oktawę uroczystości Wszystkich Świętych) oraz całoroczne.
        Pamiętajmy o&nbsp;modlitwie jako najważniejszej formie wdzięczności
        dla naszych zmarłych.</p>
        <p>11. Zapraszamy na&nbsp;zajęcia sportowe w&nbsp;piłkę nożną halową.
        Młodzież w&nbsp;poniedziałek w&nbsp;godz.&nbsp;19:30-21:00
        na&nbsp;hali pod balonem przy szkole podstawowej nr&nbsp;1,
        ul.&nbsp;Okrzei, a&nbsp;dzieci w&nbsp;sobotę,
        w&nbsp;godz.&nbsp;14:00-16:00 na&nbsp;hali przy szkole podstawowej
        nr&nbsp;4, ul.&nbsp;Cieśli (wejście od&nbsp;strony bloków).</p>
        <p>12. W&nbsp;tym tygodniu swoją ziemską pielgrzymkę zakończył nasz
        parafianin Robert Mikołajczyk z&nbsp;Kobiernik. Polećmy
        go&nbsp;miłosierdziu Bożemu.</p>
        <p>13. Wszystkim obchodzącym w&nbsp;tym tygodniu imieniny, rocznice
        i&nbsp;jubileusze składamy serdeczne życzenia. Wszystkim parafianom
        i&nbsp;gościom życzymy błogosławionej niedzieli.</p>
