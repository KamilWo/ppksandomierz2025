        <h3>12.&nbsp;października 2025&nbsp;r.</h3>
        <h3>XXVIII&nbsp;Niedziela Zwykła, Rok&nbsp;C</h3>
    </div>
    <div class="col">
        <p>1. Nabożeństwo różańcowe przez cały miesiąc październik
        o&nbsp;godz.&nbsp;17.30. Zapraszamy do&nbsp;wspólnej modlitwy całe
        rodziny: dzieci, młodzież i&nbsp;dorosłych.</p>
        <p>2. W&nbsp;tym tygodniu czcimy świętych: w&nbsp;poniedziałek
        błogosławionego Honorata Koźmińskiego, w&nbsp;środę świętą Teresę
        z&nbsp;Avila, w&nbsp;czwartek świętą Jadwigę Śląską, w&nbsp;piątek
        świętego Ignacego z&nbsp;Antiochii i&nbsp;w&nbsp;sobotę świętego
        Łukasza Ewangelistę.</p>
        <p>3. Dzisiaj Dzień Papieski – swoją ofiarą można
        wesprzeć&nbsp;Fundację „Dzieło Nowego Tysiąclecia”.</p>
        <p>4. Jutro swoje imieniny obchodzi biskup senior Edward Frankowski.
        Pamiętajmy o&nbsp;Nim w&nbsp;naszych modlitwach.</p>
        <p>5. Duszpasterstwo Kobiet diecezji sandomierskiej zaprasza
        na&nbsp;Mszę świętą w&nbsp;intencji kobiet oraz na&nbsp;wykład
        <i>Pobożność maryjna i&nbsp;jej rola w&nbsp;życiu kobiety</i>. Poniedziałek
        13&nbsp;października o&nbsp;godz.&nbsp;19.00 w&nbsp;kościele świętego
        Józefa.</p>
        <p>6. Na&nbsp;modlitwę różańcową w&nbsp;tym tygodniu zapraszamy:
        <strong>we&nbsp;wtorek</strong> – dzień Komisji Edukacji Narodowej
        – wszystkich nauczycieli i&nbsp;wychowawców,
        <strong>w&nbsp;środę</strong> – Dzień Dziecka Utraconego – rodziców,
        którzy utracili swoje dzieci, a&nbsp;<strong>w&nbsp;sobotę</strong>
        wszystkich pracowników Służby Zdrowia: lekarzy, pielęgniarki,
        pracowników szpitali, domów opieki i&nbsp;hospicjów, w&nbsp;święto ich
        Patrona św.&nbsp;Łukasza.</p>
        <p>7. Wszystkie dzieci ze&nbsp;szkoły podstawowej, chłopców
        i&nbsp;dziewczęta, zapraszamy na&nbsp;spotkanie scholi, w&nbsp;piątek
        o&nbsp;godz.&nbsp;16.30 w&nbsp;Sali na&nbsp;plebanii.</p>
        <p>8. Zapraszamy dzieci na zajęcia sportowe w&nbsp;piłkę nożną halową
        w&nbsp;najbliższą sobotę, 18&nbsp;października od&nbsp;godz.&nbsp;14.00
        do&nbsp;16.00. Zajęcia odbywają się na&nbsp;hali przy szkole
        podstawowej nr&nbsp;4, ul.&nbsp;Cieśli (wejście od&nbsp;strony
        bloków).</p>
        <p>9. Kandydatów do&nbsp;bierzmowania z&nbsp;klas 6, 7, i&nbsp;8,
        i&nbsp;ich rodziców zapraszamy <strong>w&nbsp;przyszłą niedzielę
        19&nbsp;października</strong>. Msza święta o&nbsp;godz.&nbsp;12.30,
        a&nbsp;następnie spotkanie w&nbsp;kościele.</p>
        <p>10. Po&nbsp;każdej Mszy Świętej oraz w&nbsp;godzinach urzędowania
        kancelarii, przyjmujemy wypominki jednorazowe oraz roczne. Składając
        prośbę o&nbsp;wypominki pamiętajmy o&nbsp;tym,
        że&nbsp;to&nbsp;szczególny wyraz miłości i&nbsp;wyjątkowy sposób
        modlitwy za&nbsp;tych, którzy już odeszli. Na&nbsp;stoliku pod chórem
        są&nbsp;do&nbsp;zabrania kartki wypominkowe.</p>
        <p>11. Dyrektor wydziału duszpasterstwa rodzin, a&nbsp;zarazem nasz
        Wikariusz, ksiądz Krystian zaprasza na&nbsp;Jubileuszową Pielgrzymkę
        Małżeństw i&nbsp;Rodzin, która odbędzie się 26&nbsp;października
        w&nbsp;Sanktuarium w&nbsp;Sulisławicach. O&nbsp;godz.&nbsp;12.30 Msza
        Święta z&nbsp;odnowieniem przyrzeczeń małżeńskich. Szczegóły
        na&nbsp;plakacie.</p>
        <p>12. Dyrektor domu katolickiego zaprasza na&nbsp;komedię Aleksandra
        Fredry Dożywocie w&nbsp;wykonaniu Teatru Klasyki Polskiej. Sztuka
        będzie grana w&nbsp;Domu Katolickim 22&nbsp;października
        o&nbsp;godz.&nbsp;19.00. Bilety w&nbsp;cenie 50&nbsp;zł.</p>
        <p>13. Cieszy nas duże zainteresowanie pielgrzymką do&nbsp;Portugalii,
        która odbędzie się w&nbsp;dn.&nbsp;27&nbsp;czerwca – 3&nbsp;lipca
        2026&nbsp;r. Zostało jeszcze kilka wolnych miejsc. Jeśli jednak będzie
        wystarczająca liczba chętnych to&nbsp;postaramy się zorganizować
        wyjazd dla drugiej grupy. Plan pielgrzymki został wywieszony
        w&nbsp;gablocie. Szczegółowe informacje i&nbsp;zapisy
        u&nbsp;ks.&nbsp;Krystiana.</p>
        <p>14. W&nbsp;tym roku 17&nbsp;września minęło dokładnie 25&nbsp;lat
        od&nbsp;konsekracji naszej parafialnej świątyni. Dokładna data dzienna
        wypadła w&nbsp;tym roku w&nbsp;kilka dni po&nbsp;śmierci księdza
        Proboszcza Stanisława, nie był to&nbsp;więc dobry czas
        na&nbsp;świętowanie. W&nbsp;związku z&nbsp;tym chciałbym wam
        zaproponować, abyśmy ostatnią niedzielę października przeżyli jako
        niedzielę wdzięczności Panu Bogu za&nbsp;to&nbsp;miejsce,
        w&nbsp;którym gromadzimy się na&nbsp;modlitwie, za&nbsp;budowniczych
        kościoła, za&nbsp;wszystkich parafian, którzy z&nbsp;ogromnym
        zaangażowaniem doprowadzili do&nbsp;powstania naszej świątyni.
        W&nbsp;tym dniu na&nbsp;Mszy świętej o&nbsp;godzinie 11.15 będziemy
        też mogli pomodlić się za&nbsp;i&nbsp;z&nbsp;Księdzem Proboszczem
        seniorem Józefem Śmiglem, który ten rok obchodzi także jako
        jubileuszowy 50 rok swojego kapłaństwa.</p>
        <p>15. Chciałbym bardzo serdecznie podziękować paniom z&nbsp;Kół
        Gospodyń Wiejskich z&nbsp;Milczan i&nbsp;Kobiernik, Legionu Maryi oraz
        wszystkim innym osobom, które zaangażowany się w&nbsp;sprzątanie
        kościoła w&nbsp;minionym tygodniu.</p>
        <p>16. W&nbsp;tym tygodniu ziemską pielgrzymkę zakończył nasz
        parafianin Jan Wojciech Gajda. Polećmy go&nbsp;miłosierdziu Bożemu.</p>
        <p>17. Wszystkim obchodzącym imieniny, rocznice i&nbsp;jubileusze
        składamy serdeczne życzenia. Wszystkim parafianom i&nbsp;gościom
        życzymy błogosławionej niedzieli.</p>
