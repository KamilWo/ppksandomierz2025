        <h3>26.&nbsp;października 2025&nbsp;r.</h3>
        <h3>Rocznica poświęcenia kościoła</h3>
    </div>
    <div class="col">
        <p>1. W&nbsp;najbliższą sobotę – <strong>1&nbsp;listopada</strong> –
        Uroczystość Wszystkich Świętych. W&nbsp;naszym parafialnym kościele
        porządek Mszy&nbsp;św. jak w&nbsp;niedzielę. <strong>Nie będzie
        w&nbsp;tym dniu Mszy o&nbsp;godzinie 12.30</strong>.</p>
        <p>2. <strong>1&nbsp;listopada</strong> na&nbsp;cmentarzach
        sandomierskich będą sprawowane Msze Święte: na&nbsp;cmentarzu
        komunalnym o&nbsp;godz.&nbsp;12.30, na&nbsp;cmentarzu katedralnym
        o&nbsp;godz.&nbsp;13.00 i&nbsp;Świętopawelskim o&nbsp;godz.&nbsp;14.00.</p>
        <p>3. <strong>2&nbsp;listopada</strong> – Wspomnienie wszystkich
        wiernych zmarłych, tzw. dzień zaduszny – Msze święte w&nbsp;naszym
        kościele <strong>jak w&nbsp;każdą niedzielę</strong>.</p>
        <p>4. <strong>2&nbsp;listopada Mszę świętą o&nbsp;godzinie 9.30</strong>
        będziemy sprawować w&nbsp;intencji zmarłych parafian. Po&nbsp;Mszy
        Świętej przejdziemy w&nbsp;procesji zadusznej aby pomodlić się
        szczególnie za osoby zmarłe w&nbsp;tym roku. Na&nbsp;tę&nbsp;Mszę
        świętą zapraszamy zwłaszcza tych, którzy w&nbsp;tym roku pożegnali
        swoich najbliższych.</p>
        <p>5. 1&nbsp;listopada przypada pierwsza sobota miesiąca. Wyjątkowo
        <strong>nie będzie tego dnia adoracji i&nbsp;wspólnego różańca
        o&nbsp;17.30</strong>. Zachęcamy, aby wynagradzający różaniec
        i&nbsp;rozmyślanie odprawić indywidualnie.</p>
        <p>6. Również wyjątkowo, 2&nbsp;listopada, czyli w&nbsp;przyszłą
        niedzielę, <strong>nie będzie różańca o&nbsp;godzinie 10.45</strong>.
        Zmiana tajemnic różańcowych tego dnia po&nbsp;Mszy Świętej
        o&nbsp;godzinie 11.15.</p>
        <p>7. 2&nbsp;listopada przypada rocznica śmierci Sługi Bożego Biskupa
        Piotra Gołębiowskiego – módlmy się o&nbsp;jego rychłą beatyfikację.</p>
        <p>8. Każdego dnia od&nbsp;1 do&nbsp;8&nbsp;listopada o&nbsp;godzinie
        17.30 Koronka do&nbsp;Bożego Miłosierdzia za&nbsp;zmarłych polecanych
        w&nbsp;wypominkach jednorazowych.</p>
        <p>9. Przypominamy o&nbsp;możliwości zyskania odpustu zupełnego
        i&nbsp;ofiarowania go&nbsp;za&nbsp;zmarłych od&nbsp;1
        do&nbsp;8&nbsp;listopada. Warunkiem uzyskania każdego odpustu jest
        wyzbycie się przywiązania do&nbsp;grzechu, <strong>dlatego zachęcamy,
        aby przed Uroczystością Wszystkich Świętych skorzystać
        z&nbsp;sakramentu pokuty</strong>. Warunkiem uzyskania odpustu jest
        nawiedzenie cmentarza i&nbsp;modlitwa za&nbsp;zmarłych oraz wypełnienie
        zwykłych warunków, tj.&nbsp;przystąpienie do&nbsp;komunii św.
        w&nbsp;dniu nawiedzenia cmentarza, modlitwa "Ojcze nasz" i&nbsp;"Wierzę
        w&nbsp;Boga", i&nbsp;jakiejkolwiek modlitwa w&nbsp;intencjach
        wyznaczonych na&nbsp;dany dzień przez papieża.</p>
        <p>10. <strong>W&nbsp;środę 29&nbsp;października</strong>
        o&nbsp;godz.&nbsp;19.00 w&nbsp;salce na&nbsp;plebanii odbędzie się
        spotkanie Stowarzyszenia Przyjaciół WSD w&nbsp;Sandomierzu.</p>
        <p>11. Spotkanie scholi w&nbsp;piątek o&nbsp;godz.&nbsp;16.30
        w&nbsp;salce na&nbsp;plebanii.</p>
        <p>12. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka oraz
        kalendarze z&nbsp;naszej drukarni diecezjalnej w&nbsp;cenie
        5&nbsp;zł.</p>
        <p>13. Bardzo dziękujemy Paniom za&nbsp;posprzątanie kościoła
        na&nbsp;dzisiejszą niedzielę.</p>
        <p>14. Chcielibyśmy bardzo podziękować za&nbsp;Waszą ofiarność. Dwa
        tygodnie temu na&nbsp;Fundację Dzieło Nowego Tysiąclecia do&nbsp;puszek
        zebraliśmy kwotę 3467&nbsp;zł, natomiast na&nbsp;tacę misyjną tydzień
        temu 7371&nbsp;zł. <em>Bóg zapłać!</em></p>
        <p>15. W&nbsp;tym tygodniu swoją ziemską pielgrzymkę zakończyli nasi
        parafianie: Mateusz Frańczak z&nbsp;Kobiernik, Wacław Borycki
        z&nbsp;Milczan oraz Stanisław Mucha z&nbsp;Sandomierza. Polećmy ich
        Bożemu miłosierdziu: <em>Wieczny odpoczynek…</em></p>
        <p>16. Wszystkim obchodzącym w&nbsp;tym tygodniu imieniny, rocznice
        i&nbsp;jubileusze składamy serdeczne życzenia. Wszystkim parafianom
        i&nbsp;gościom życzymy błogosławionej niedzieli.</p>
