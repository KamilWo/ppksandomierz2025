        <p>1. Dziś ostatnia niedziela roku liturgicznego.</p>
        <p>2. W&nbsp;przyszłą niedzielę rozpoczynamy Adwent, okres
        szczególnego oczekiwania na&nbsp;powtórne przyjście Chrystusa oraz
        przygotowania do&nbsp;Świąt Bożego Narodzenia.</p>
        <p>3. Mszę świętą roratnią przeżywać będziemy w&nbsp;Adwencie
        w&nbsp;dni powszednie o&nbsp;godz.&nbsp;6.20. Natomiast godzinki
        będziemy śpiewać o&nbsp;godzinie 6.00.</p>
        <p>4. W&nbsp;najbliższy wtorek <strong>25&nbsp;listopada</strong>
        ks.&nbsp;Paweł Lasek zaprasza na&nbsp;kolejne spotkanie Akademii
        Biblijnej w&nbsp;Sandomierzu. Tematem tego spotkania będzie:
        <strong>List św.&nbsp;Pawła do&nbsp;Galatów.</strong> Początek
        o&nbsp;godz.&nbsp;18.45, w&nbsp;auli Wyższego Seminarium Duchownego.
        W&nbsp;spotkaniu będzie można uczestniczyć także internetowo przez
        Facebooka na&nbsp;profilu WSD Sandomierz.</p>
        <p>5. Próba scholii w&nbsp;piątek o&nbsp;godz.&nbsp;17.00.</p>
        <p>6. W&nbsp;przyszłą sobotę <strong>29&nbsp;listopada</strong>
        zapraszamy kandydatów do&nbsp;bierzmowania z&nbsp;klas&nbsp;VI i&nbsp;VII
        na&nbsp;spotkanie formacyjne. Początek o&nbsp;godzinie 9.00.
        Zakończenie o&nbsp;godzinie 13.00.</p>
        <p>7. Także w&nbsp;przyszłą sobotę zapraszamy na&nbsp;kolejny Wieczór
        Uwielbienia. Początek o&nbsp;godz.&nbsp;18.00. Tym razem będziemy
        gościć grupę ewangelizacyjną Teobańkologia z&nbsp;ks.&nbsp;Teodorem.
        Szczegóły na&nbsp;plakacie. Zapraszamy do&nbsp;wspólnej modlitwy.</p>
        <p>8. Wydział Duszpasterstwa Rodzin zaprasza na&nbsp;Warsztaty dla
        małżeństw, które odbędą się w&nbsp;Ostrowcu Świętokrzyskim
        w&nbsp;dniach 5, 12 i&nbsp;19&nbsp;grudnia (piątek). Szczegółowe
        informacje na&nbsp;plakacie.</p>
        <p>9. W&nbsp;sobotę – 6&nbsp;grudnia – odbędzie się w&nbsp;naszej
        parafii Dzień Skupienia dla osób żyjących w&nbsp;związkach
        niesakramentalnych. Zainteresowane osoby, prosimy o&nbsp;kontakt
        z&nbsp;ks.&nbsp;Krystianem.</p>
        <p>10. Dziś można wesprzeć chorą na&nbsp;zespół Retta Amelkę. Przy
        wyjściach z&nbsp;kościoła na&nbsp;jej rzecz kwestują wolontariusze
        z&nbsp;Fundacji Się Pomaga. Dziękuję w&nbsp;imieniu rodziców
        za&nbsp;okazane wsparcie.</p>
        <p>11. W&nbsp;tym tygodniu swoją ziemską pielgrzymkę zakończyli nasi
        parafianie: Zofia Skorupska z&nbsp;Kobiernik oraz Mieczysław Frydel
        z&nbsp;Sandomierza. Polećmy ich Bożemu miłosierdziu…</p>
        <p>12. Dziękujemy za&nbsp;wszelkie duchowe i&nbsp;materialne wsparcie
        naszej parafii. Wszystkim obchodzącym w&nbsp;tym tygodniu imieniny,
        rocznice i&nbsp;jubileusze składamy serdeczne życzenia. Naszym
        Parafianom i&nbsp;Gościom życzymy błogosławionej niedzieli.</p>
