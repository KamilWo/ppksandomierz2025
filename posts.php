        <p>1. Gościmy dziś grupę ewangelizacyjną z&nbsp;księdzem Teodorem
        Sawielewiczem. Dziękujemy za&nbsp;głoszenie Słowa Bożego
        i&nbsp;świadectwo wiary.</p>
        <p>2. Rozpoczęliśmy Adwent. Od&nbsp;jutra w&nbsp;dni powszednie
        o&nbsp;godzinie 6.00 śpiewać będziemy Godzinki ku&nbsp;czci
        Niepokalanego Poczęcia NMP, a&nbsp;roratnią Mszę Świętą będziemy
        rozpoczynali o&nbsp;godz.&nbsp;6.20.</p>
        <p>3. W&nbsp;tym tygodniu wypadają pierwszy czwartek, piątek
        i&nbsp;sobota miesiąca. W&nbsp;czwartek adoracja Pana Jezusa
        w&nbsp;Najświętszym Sakramencie od&nbsp;godz.&nbsp;17.30,
        w&nbsp;piątek spowiedź od&nbsp;godziny 17.00, natomiast w&nbsp;sobotę
        o&nbsp;17.00 wystawimy Najświętszy Sakrament, będzie możliwość
        modlitwy w&nbsp;ciszy, a&nbsp;o&nbsp;17.30 odmówimy różaniec
        w&nbsp;intencji wynagradzającej.</p>
        <p>4. W&nbsp;piątek nie będzie odwiedzin chorych w&nbsp;domach.
        Odwiedziny i&nbsp;spowiedź przedświąteczną chorych planujemy
        na&nbsp;22&nbsp;grudnia.</p>
        <p>5. Wydział Duszpasterstwa Rodzin zaprasza na&nbsp;Warsztaty dla
        małżeństw, które odbędą się w&nbsp;Ostrowcu Świętokrzyskim
        w&nbsp;dniach 5, 12 i&nbsp;19&nbsp;grudnia (piątek). Szczegółowe
        informacje na&nbsp;plakacie.</p>
        <p>6. W&nbsp;sobotę – 6&nbsp;grudnia – odbędzie się w&nbsp;naszej
        parafii Dzień Skupienia dla osób żyjących w&nbsp;związkach
        niesakramentalnych. Zainteresowane osoby, prosimy o&nbsp;kontakt
        z&nbsp;ks.&nbsp;Krystianem.</p>
        <p>7. W&nbsp;sobotę – 13&nbsp;grudnia – w&nbsp;parafii
        pw.&nbsp;Św.&nbsp;Floriana w&nbsp;Stalowej Woli odbędzie się Dzień
        skupienia dla osób, które utraciły kogoś bliskiego i&nbsp;przeżywają
        żałobę. W&nbsp;planie m.in.: konferencja duchowa, spotkanie
        z&nbsp;psychologiem o&nbsp;tym jak przeżywać żałobę. Szczegółowe
        informacje na&nbsp;plakacie lub u&nbsp;ks.&nbsp;Krystiana.</p>
        <p>8. Wspólnota Serce Kecharitomene zaprasza dzieci do&nbsp;wspólnej
        modlitwy i&nbsp;formacji w&nbsp;duchu pierwszych sobót miesiąca.
        Spotkanie w&nbsp;najbliższą sobotę 6&nbsp;grudnia
        o&nbsp;godz.&nbsp;16.00 w&nbsp;kościele św.&nbsp;Józefa
        w&nbsp;Sandomierzu.</p>
        <p>9. Tydzień temu złożyliście na&nbsp;rzecz chorej Amelki
        7530&nbsp;zł. Rodzice, którzy wraz z&nbsp;wolontariuszami kwestowali
        tydzień temu na&nbsp;rzecz chorej córki dziękują bardzo
        za&nbsp;okazane wsparcie.</p>
        <p>10. Przyszła niedziela to&nbsp;od&nbsp;wielu lat Dzień Modlitwy
        i&nbsp;Pomocy Kościołowi na&nbsp;Wschodzie, w&nbsp;krajach byłego
        Związku Sowieckiego. Po&nbsp;wszystkich Mszach będą zbierane ofiary
        do&nbsp;puszek na&nbsp;ten cel.</p>
        <p>11. W&nbsp;przyszłym tygodniu będziemy gościli wolontariuszy
        z&nbsp;domu dziecka w&nbsp;Tarnopolu na&nbsp;Ukrainie, który będą
        prowadzili zbiórkę na&nbsp;pomoc dzieciom osieroconym przez wojnę.</p>
        <p>12. W&nbsp;zakrystii można od&nbsp;dzisiaj odebrać opłatki oraz
        nabyć świece Caritas w&nbsp;cenie 15&nbsp;zł. mała i&nbsp;30&nbsp;zł.
        duża.</p>
        <p>13. Dziękujemy za&nbsp;wszelkie duchowe i&nbsp;materialne wsparcie
        naszej parafii. Wszystkim obchodzącym w&nbsp;tym tygodniu imieniny,
        rocznice i&nbsp;jubileusze składamy serdeczne życzenia. Naszym
        Parafianom i&nbsp;Gościom życzymy błogosławionej niedzieli.</p>
