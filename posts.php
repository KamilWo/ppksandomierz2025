        <h3>21.&nbsp;grudzień 2025&nbsp;r.</h3>
        <h3>IV Niedziela Adwentu</h3>
    </div>
    <div class="col">
        <p>1. Dobiega końca nasze adwentowe oczekiwanie. W&nbsp;środę Wigilia,
        a&nbsp;w&nbsp;czwartek Uroczystość Bożego Narodzenia.</p>
        <p>2. W&nbsp;poniedziałek <strong>22&nbsp;grudnia</strong> odwiedzimy
        chorych zarówno tych zapisanych w&nbsp;zakrystii, jak i&nbsp;tych,
        których odwiedzamy w&nbsp;pierwsze piątki miesiąca. Początek
        o&nbsp;godz.&nbsp;8.00.</p>
        <p>3. Tego też dnia (poniedziałek <strong>22&nbsp;grudnia</strong>)
        na&nbsp;naszą parafię przypada dzień modlitw w&nbsp;intencji powołań
        kapłańskich. Nie będziemy organizować specjalnego nabożeństwa.
        Zapraszamy do&nbsp;indywidualnej adoracji Pana Jezusa
        w&nbsp;Najświętszym Sakramencie. Pan Jezus będzie wystawiony
        na&nbsp;ołtarzu od&nbsp;godz.&nbsp;7.00 do&nbsp;18.00. Zachęcam, żeby
        w&nbsp;przedświątecznej bieganinie znaleźć chwilę na&nbsp;wyciszenie
        i&nbsp;osobiste spotkanie z&nbsp;Bogiem.</p>
        <p>4. W&nbsp;Wigilię Bożego Narodzenia Msza Święta tylko rano,
        o&nbsp;godz.&nbsp;6.20.</p>
        <p>5. W&nbsp;Boże Narodzenie Pasterka o&nbsp;północy. Przed Pasterką,
        na&nbsp;godzinę 23.40 zapraszamy na&nbsp;czuwanie kolędowe, wspólny
        śpiew kolęd. Msze Święte tego dnia w&nbsp;porządku niedzielnym.</p>
        <p>6. W&nbsp;piątek, w&nbsp;święto św.&nbsp;Szczepana, Msze Święte
        w&nbsp;porządku niedzielnym. <strong>Nie będzie Mszy
        o&nbsp;godz.&nbsp;18.00.</strong></p>
        <p>7. Na&nbsp;dzień <strong>26&nbsp;grudnia</strong> (piątek) Biskup
        Ordynariusz udzielił dyspensy od&nbsp;obowiązku powstrzymania się
        od&nbsp;spożywania pokarmów mięsnych. Jednocześnie zachęca wiernych,
        którzy skorzystają z&nbsp;dyspensy, do&nbsp;odmówienia dowolnej
        modlitwy w&nbsp;intencji prześladowanych chrześcijan.</p>
        <p>8. W&nbsp;sobotę <strong>27&nbsp;grudnia</strong> zapraszamy
        na&nbsp;kolejny Wieczór Uwielbienia. Początek
        o&nbsp;godz.&nbsp;18.00</p>
        <p>9. W&nbsp;przyszłą niedzielę <strong>28&nbsp;grudnia</strong>,
        w&nbsp;Bazylice Katedralnej będzie miało miejsce zakończenie Roku
        Jubileuszowego Msza Święta pod przewodnictwem Biskupa Ordynariusza
        będzie sprawowana o&nbsp;godz.&nbsp;10.30. Wydarzenie to&nbsp;jest
        równocześnie Diecezjalnym Świątecznym Spotkaniem Rodzin.</p>
        <p>10. 6&nbsp;stycznia w&nbsp;Uroczystość Objawienia Pańskiego Orszak
        Trzech Króli, który w&nbsp;tym roku odbędzie się pod hasłem "Nadzieją
        się cieszą". Poszczególne kolumny orszaku wyruszą o&nbsp;godzinie
        12.00 z&nbsp;parafii: Podwyższenia Krzyża Świętego, Nawrócenia
        św.&nbsp;Pawła Apostoła i&nbsp;Matki Bożej Królowej Polski. Około
        godziny 13.00 kolumny dotrą na&nbsp;Rynek Starego Miasta, gdzie
        zakończymy Orszak wspólną modlitwą oraz pokłonem Mędrców.</p>
        <p>11. Przypominamy, że&nbsp;do&nbsp;końca grudnia należy uregulować
        II&nbsp;ratę za&nbsp;pielgrzymkę do&nbsp;Portugalii.</p>
        <p>12. W&nbsp;zakrystii można odebrać opłatki i&nbsp;świece Caritas.</p>
        <p>13. Przy wyjściach z&nbsp;kościoła jest przygotowane
        do&nbsp;zabrania sianko na&nbsp;stół wigilijny. Sianko i&nbsp;życzenia
        od&nbsp;duszpasterzy spakowała dla parafian młodzież z&nbsp;klas&nbsp;6
        i&nbsp;7&nbsp;przygotowująca się do&nbsp;bierzmowania. Bardzo dziękuję
        młodym za&nbsp;zaangażowanie, a&nbsp;księdzu Wojtkowi
        za&nbsp;inspirację i&nbsp;dostarczenie sianka. Na&nbsp;każdych
        życzeniach oprócz podpisów księży jest podpis jednego
        z&nbsp;kandydatów do&nbsp;bierzmowania. Proszę bardzo, aby wyrazić
        wdzięczność młodym za&nbsp;ich prace poprzez krótką modlitwę
        w&nbsp;ich intencji.</p>
        <p>14. Bardzo dziękuję za&nbsp;przygotowanie kościoła do&nbsp;świąt.
        Posprzątanie, przygotowanie szopki, dekoracji, dostarczenie, osadzenie
        i&nbsp;ubranie choinek… Nie wymieniam imiennie, aby nikogo nie
        pominąć. Niech Pan Bóg hojnie wam wynagrodzi wasze zaangażowanie.</p>
        <p>15. Chcemy od&nbsp;soboty <strong>27&nbsp;grudnia</strong>
        rozpocząć wizytę duszpasterską. Plan na&nbsp;pierwszy etap kolędy jest
        wywieszony w&nbsp;gablocie i&nbsp;na&nbsp;stronie internetowej.
        W&nbsp;razie pytań co&nbsp;do&nbsp;przebiegu kolędy proszę
        o&nbsp;kontakt z&nbsp;księdzem Krystianem.</p>
        <p>16. Dziękujemy za&nbsp;wszelkie duchowe i&nbsp;materialne wsparcie
        naszej parafii. Za&nbsp;ofiary składane na&nbsp;tacę, osobiście czy
        przez konto parafialne. <em>Bóg zapłać</em>! Wszystkim obchodzącym
        imieniny, rocznice i&nbsp;jubileusze składamy serdeczne życzenia.
        Naszym parafianom i&nbsp;gościom życzymy błogosławionej niedzieli.</p>
        <p>
