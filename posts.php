        <h3>14.&nbsp;grudzień 2025&nbsp;r.</h3>
        <h3>III Niedziela Adwentu</h3>
    </div>
    <div class="col">
        <p>1. Przed nami ostatni pełny tydzień adwentu.
        Od&nbsp;16&nbsp;grudnia liturgia wprowadza nas w&nbsp;bezpośrednie
        przygotowanie do&nbsp;Świąt Bożego Narodzenia.</p>
        <p>2. W&nbsp;<strong>czwartek 18&nbsp;grudnia</strong> katecheza
        na&nbsp;temat dekalogu dla rodziców bierzmowanych i&nbsp;dla
        wszystkich chętnych.</p>
        <p>3. Spowiedź adwentowa w&nbsp;naszym kościele w&nbsp;<strong>piątek
        19&nbsp;grudnia</strong>, w&nbsp;godz.&nbsp;10.00-11.00, 16.30-18.00,
        oraz po&nbsp;Mszy Świętej 19.00 – 20.00. Bardzo zachęcamy, żeby
        skorzystać z&nbsp;sakramentu pojednania przed niedzielą, tak, żeby
        niedzielną Eucharystię przeżywać w&nbsp;skupieniu, a&nbsp;nie
        w&nbsp;długiej kolejce do&nbsp;konfesjonału. W&nbsp;gablocie
        wywieszona jest także informacja o&nbsp;terminach spowiedzi
        w&nbsp;innych kościołach Sandomierza.</p>
        <p>4. W&nbsp;najbliższą <strong>sobotę 20&nbsp;grudnia</strong>
        zapraszamy kandydatów do&nbsp;bierzmowania z&nbsp;klas 6
        i&nbsp;7&nbsp;na&nbsp;spotkanie formacyjne. Spotkanie potrwa
        od&nbsp;godz.&nbsp;10.00 do&nbsp;12.00. Początek w&nbsp;salce
        na&nbsp;plebanii.</p>
        <p>5. Odwiedziny chorych przed świętami planujemy na&nbsp;przyszły
        <strong>poniedziałek 22&nbsp;grudnia</strong>. Prosimy
        o&nbsp;zapisywanie chorych w&nbsp;zakrystii. Nie trzeba zapisywać
        tych chorych, do&nbsp;których jeździmy w&nbsp;pierwsze piątki
        miesiąca.</p>
        <p>6. W&nbsp;najbliższy wtorek, jak w&nbsp;każdy trzeci wtorek
        miesiąca w&nbsp;tym roku, zapraszamy na&nbsp;kolejne spotkanie
        Akademii Biblijnej w&nbsp;Sandomierzu; poprowadzi je&nbsp;wykładowca
        Pisma świętego w&nbsp;naszym seminarium, ks.&nbsp;Paweł Lasek.
        Tematem tego spotkania będzie: List św.&nbsp;Pawła do&nbsp;Filipian.
        Spotkanie odbędzie się we&nbsp;<strong>wtorek,
        16&nbsp;grudnia</strong>, o&nbsp;godz.&nbsp;18.45, w&nbsp;auli
        Wyższego Seminarium Duchownego. W&nbsp;spotkaniach będzie można
        uczestniczyć także internetowo, będą ono transmitowane przez
        Facebooka, na&nbsp;profilu WSD Sandomierz. Serdecznie zapraszamy!</p>
        <p>7. Jak co&nbsp;roku dnia 6&nbsp;stycznia w&nbsp;Uroczystość
        Objawienia Pańskiego wspólnie przeżyjemy jedno z&nbsp;najpiękniejszych
        wydarzeń w&nbsp;kalendarzu bożonarodzeniowym - Orszak Trzech Króli,
        który w&nbsp;tym roku odbędzie się pod hasłem "Nadzieją się cieszą".
        Poszczególne kolumny orszaku wyruszą o&nbsp;godzinie 12.00
        z&nbsp;parafii: Podwyższenia Krzyża Świętego, Nawrócenia
        św.&nbsp;Pawła Apostoła i&nbsp;Matki Bożej Królowej Polski. Około
        godziny 13.00 kolumny dotrą na&nbsp;Rynek Starego Miasta, gdzie
        zakończymy Orszak wspólną modlitwą oraz pokłonem Mędrców.</p>
        <p>8. Dziękuję bardzo za&nbsp;Waszą ofiarność. Tydzień temu
        do&nbsp;puszki na&nbsp;rzecz Kościoła na&nbsp;wschodzie złożyliście
        2233&nbsp;zł i&nbsp;10&nbsp;euro. Nie otrzymałem jeszcze informacji
        o&nbsp;środkach zebranych przez wolontariuszy na&nbsp;dzieci ofiary
        wojny.</p>
        <p>9. W&nbsp;zakrystii można odebrać opłatki i&nbsp;świece Caritas.</p>
        <p>10. W&nbsp;ostatnim czasie swoją ziemską pielgrzymkę zakończyli
        nasi parafianie Marian Cąbrowski i&nbsp;Kazimierz Woźniak. Polećmy
        ich Bożemu miłosierdziu.</p>
        <p>11. Dziękujemy za&nbsp;wszelkie duchowe i&nbsp;materialne wsparcie
        naszej parafii. Wszystkim obchodzącym imieniny, rocznice
        i&nbsp;jubileusze składamy serdeczne życzenia. Naszym parafianom
        i&nbsp;gościom życzymy błogosławionej niedzieli.</p>
        <p>
            <span style="float: left;">Sandomierz, 14.&nbsp;grudzień 2025&nbsp;r.</span>
            <span style="float: right; text-align: right;">ks. proboszcz Rafał Kobiałka</span>
        </p>
    </div>
